/**
 * 根据系列名和集数查找剧集标题（大小写不敏感）
 * @returns 标题字符串，未找到返回 null
 */
export function lookupTitle(seriesName: string, episodeNumber: number): string | null {
  // 先精确匹配
  let series = TITLES[seriesName]
  // 大小写不敏感回退：目录名可能是"假面骑士w"但数据库key是"假面骑士W"
  if (!series) {
    const key = Object.keys(TITLES).find(
      (k) => k.toLowerCase() === seriesName.toLowerCase()
    )
    if (key) series = TITLES[key]
  }
  if (!series) return null
  return series[episodeNumber] ?? null
}

/**
 * 检查是否有某系列的标题数据（大小写不敏感）
 */
export function hasSeriesTitles(seriesName: string): boolean {
  if (seriesName in TITLES) return true
  return Object.keys(TITLES).some(
    (k) => k.toLowerCase() === seriesName.toLowerCase()
  )
}

export default TITLES
